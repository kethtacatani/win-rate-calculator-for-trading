    public void displayLoad(){
       
        
        try {
            
            ResultSet rs;
            
            rs = main.loadData();
            
            while(rs.next()){
            dim.addElement(rs.getString("name"));
                    }
            
            loadList.setModel(dim);
            rs.close();
            

          
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoadFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoadFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
  
    }
    
    
