    public void deleteData() throws ClassNotFoundException, SQLException{
        con = main.getCOnnection();
        PreparedStatement prep = con.prepareStatement("DELETE from `nameDB` WHERE `nameDB`.`name` = '"+getName()+"'");
        prep.executeUpdate();
        
    }
    
