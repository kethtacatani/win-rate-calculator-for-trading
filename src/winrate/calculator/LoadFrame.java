    private void deleteBTnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTnActionPerformed
        // TODO add your handling code here:
        getName();
         int confirm = JOptionPane.showConfirmDialog(null, "Confirm delete this row?", "Delete row",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(confirm == JOptionPane.YES_OPTION){
            try {
                deleteData();
                display();
                LoadFrame load = new LoadFrame();
                this.dispose();
                load.setVisible(true);


            } catch (ClassNotFoundException ex) {
                Logger.getLogger(LoadFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(LoadFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteBTnActionPerformed

